--13. LOCATIONS 테이블에서 CITY가 a로 끝나고, STATE_PROVINCE가 NULL인 데이터를 조회하세요.
SELECT
	*
FROM
	LOCATIONS l
WHERE
	CITY LIKE '%a'
	AND STATE_PROVINCE IS NULL;

--14. LOCATIONS 테이블에서 CITY와 STATE_PROVINCE가 동일한 데이터를 조회하세요.
SELECT
	*
FROM
	LOCATIONS l
WHERE
	CITY = STATE_PROVINCE;

